# 1. Arithmetic Operators
# +, -, * , / , //, %

x = 100
y = 8

print(f"Additions of {x} and {y} is {x + y}")
print(f"Subtraction from {x},  {y} is {x - y}")
print(f"Product of {x} and {y} is {x * y}")
print(f"Division of {x} by {y} is {x / y}")
print(f"Floor Division of {x} and {y} is {x // y}")
print(f"R of {x} divided by {y} is {x % y}")

